public class Switch {
	public static void main(String[] args) {
		int value = 2;

		switch (value) {
			case 1:
				System.out.println("Value was 1");
				break;
			case 2:
				System.out.println("Value was 2");
				break;
			default:
				System.out.println("Was not 1 or 2");
				break;
			// More code here

		}
	}
}